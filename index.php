document.addEventListener("DOMContentLoaded", function() {
    const wrapper = document.querySelector('.scrolling-wrapper');
    
    if (wrapper) {
        // 1. Lấy toàn bộ mã HTML của các thẻ sản phẩm hiện có
        const originalHTML = wrapper.innerHTML;
        
        // 2. Nhân bản lên 6 lần để đường chạy siêu dài, không bao giờ bị cạn
        wrapper.innerHTML = originalHTML.repeat(6);
        
        // 3. Tính toán lại chiều dài chính xác của 1 bộ sản phẩm gốc (chia 6)
        const originalWidth = wrapper.scrollWidth / 6;
        
        let isPaused = false;
        
        // Xử lý sự kiện dừng khi rê chuột hoặc chạm
        wrapper.addEventListener('mouseenter', () => isPaused = true);
        wrapper.addEventListener('mouseleave', () => isPaused = false);
        wrapper.addEventListener('touchstart', () => isPaused = true, {passive: true});
        wrapper.addEventListener('touchend', () => isPaused = false);

        function autoScroll() {
            if (!isPaused) {
                wrapper.scrollLeft += 1.5; 
                
                // 4. Ngay khi vừa chạy qua hết độ dài của 1 bộ gốc, lập tức giật về
                if (wrapper.scrollLeft >= originalWidth) {
                    wrapper.scrollLeft -= originalWidth;
                }
            }
            requestAnimationFrame(autoScroll);
        }
        
        // Kích hoạt chạy
        autoScroll();
    }
});
</script>
